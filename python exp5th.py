# Define the string
s = "PythonProgramming"

print("Accessing characters by index:")

print("Positive indexing:")
print("s[0]:", s[0])
print("s[6]:", s[6])

print("Negative indexing:")
print("s[-1]:", s[-1])
print("s[-7]:", s[-7])


print("\nAccessing characters by slice operator:")
print("s[0:6]:", s[0:6])
print("s[6:]:", s[6:])
print("s[:6]:", s[:6])
print("s[-6:]:", s[-6:])
print("s[::2]:", s[::2])
print("s[2:11:3]:", s[2:11:3])
